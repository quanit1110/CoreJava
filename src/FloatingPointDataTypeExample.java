
public class FloatingPointDataTypeExample {
	public static void main (String [] args){
	// demonstrating float data type
		float pi, r, a;
		r = 10.80f; //radius
		pi = 3.1416f;
		a = pi * r * r;
		System.out.println ("Area of circle in float data type is " + a);
		
		//demonstrating double data type
		double pi1, r1, a1;
		r1 = 10.8;
		pi1= 3.1416;
		a1 = pi1 * r1 * r1;
		System.out.println ("Area of circle in double data type is " +a1);
	}

}
