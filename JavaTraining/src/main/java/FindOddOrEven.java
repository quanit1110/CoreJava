/** This program will demonstrate to find odd or even.
 * Created method that will return boolean value (true) in case of number is odd and (false) in case of number is an even.
 * It will take an input number from User and will calculate.
 *
 */
public class FindOddOrEven {
    public boolean isOdd ( int x){
            if (x % 2 == 0) {
                return false;
            } else {
                return true;
            }
        }
    }


